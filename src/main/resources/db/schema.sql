BEGIN
  FOR table_name IN (
    SELECT 'ANNOUNCEMENT' name FROM dual UNION ALL
    SELECT 'NOTIFICATION' FROM dual UNION ALL
    SELECT 'REVIEW' FROM dual UNION ALL
    SELECT 'GRADE' FROM dual UNION ALL
    SELECT 'ENROLLMENT' FROM dual UNION ALL
    SELECT 'WAITLIST' FROM dual UNION ALL
    SELECT 'CART_ITEM' FROM dual UNION ALL
    SELECT 'REGISTRATION_PERIOD' FROM dual UNION ALL
    SELECT 'SECTION_SCHEDULE' FROM dual UNION ALL
    SELECT 'COURSE_SECTION' FROM dual UNION ALL
    SELECT 'CLASSROOM' FROM dual UNION ALL
    SELECT 'SEMESTER' FROM dual UNION ALL
    SELECT 'PREREQUISITE' FROM dual UNION ALL
    SELECT 'COURSE' FROM dual UNION ALL
    SELECT 'ADMIN' FROM dual UNION ALL
    SELECT 'PROFESSOR' FROM dual UNION ALL
    SELECT 'STUDENT' FROM dual UNION ALL
    SELECT 'USER_ACCOUNT' FROM dual UNION ALL
    SELECT 'DEPARTMENT' FROM dual
  ) LOOP
    BEGIN
      EXECUTE IMMEDIATE 'DROP TABLE ' || table_name.name || ' CASCADE CONSTRAINTS PURGE';
    EXCEPTION
      WHEN OTHERS THEN
        IF SQLCODE != -942 THEN
          RAISE;
        END IF;
    END;
  END LOOP;
END;
