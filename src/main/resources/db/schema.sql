COMMENT ON TABLE registration_period IS '수강신청 기간 설정';
COMMENT ON COLUMN registration_period.target_year IS 'NULL이면 전체 학년 대상';

CREATE INDEX idx_registration_period_active ON registration_period (semester_id, start_at, end_at);

