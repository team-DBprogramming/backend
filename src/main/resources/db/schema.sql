COMMENT ON TABLE review IS '강의 평가';

CREATE TABLE notification (
  notification_id NUMBER(19) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  recipient_user_id NUMBER(19) NOT NULL,
  sender_user_id NUMBER(19),
  target_section_id NUMBER(19),
  target_request_id NUMBER(19),
  title VARCHAR2(200) NOT NULL,
  body CLOB,
  type VARCHAR2(30) NOT NULL,
  is_read NUMBER(1) DEFAULT 0,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT pk_notification PRIMARY KEY (notification_id),
  CONSTRAINT fk_notification_recipient FOREIGN KEY (recipient_user_id) REFERENCES user_account (user_id) ON DELETE CASCADE,
  CONSTRAINT fk_notification_sender FOREIGN KEY (sender_user_id) REFERENCES user_account (user_id) ON DELETE SET NULL,
  CONSTRAINT fk_notification_section FOREIGN KEY (target_section_id) REFERENCES course_section (section_id) ON DELETE SET NULL,
  CONSTRAINT fk_notification_request FOREIGN KEY (target_request_id) REFERENCES course_request (request_id) ON DELETE SET NULL,
  CONSTRAINT chk_notification_type CHECK (
    type IN ('COURSE_REQUEST', 'COURSE_REQUEST_RESULT', 'COURSE_REVIEW', 'PROFESSOR_MESSAGE', 'SYSTEM')
  ),
  CONSTRAINT chk_notification_read CHECK (is_read IN (0, 1))
);

COMMENT ON TABLE notification IS '공통 알림';

CREATE INDEX idx_notification_recipient_unread ON notification (recipient_user_id, is_read, created_at DESC);
CREATE INDEX idx_notification_type_created ON notification (type, created_at DESC);
